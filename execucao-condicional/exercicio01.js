//Testar se o quadrado de um número é maior do que ele multiplicado por 10

var a = 11

if(a ** 2 > a * 10){
    console.log("O quadrado do número é MAIOR que ele multiplicado por 10")
} else if(a ** 2 == a * 10){
    console.log("O quadrado do número é IGUAL a ele multiplicado por 10")
} else{
    console.log("O quadrado do número é MENOR que ele multiplicado por 10")
}